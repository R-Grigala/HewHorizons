# Initialize a variable 'count' with a starting value of 1
count = 1

# Start a loop that continues as long as 'count' is less than or equal to 10
while count <= 10:
    # Print "Hello World!" followed by the current value of 'count'
    print("Hello World!", count)
    # Increment 'count' by 1 to move to the next iteration
    count += 1